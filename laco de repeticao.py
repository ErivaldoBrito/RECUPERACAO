# Laço de Repetição
# Comando para repetir um determinado número de vezes uma ou mais linhas
# Uma ou mais linhas de código
# 

# for
# Exemplo:
for i in range(3):
    print(f"Variavel i: {i}")

print("Fim")

# While
# Exemplo:
while True:
    nome = input("Digite seu nome: ")
    if nome == "sair":
        break
    print(nome)

print("Fim.")
    
